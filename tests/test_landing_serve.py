def test_tag_e2e_round_trip(server):
    """Upload STL, get tagged STL + gcode back, verify L1 + L2 round-trip."""
    import urllib.request
    import urllib.error

    stl_bytes = _make_binary_stl()
    order_id = "ORDER-TEST-LANDING-001"
    printer = "bambucco-1"
    job_seq = 99

    # multipart/form-data via stdlib
    boundary = "----gp-test-boundary-x7y9z"
    parts: list[bytes] = []
    def add_field(name: str, value: str):
        parts.append(f"--{boundary}\r\n".encode())
        parts.append(f'Content-Disposition: form-data; name="{name}"\r\n\r\n'.encode())
        parts.append(value.encode())
        parts.append(b"\r\n")
    add_field("order_id", order_id)
    add_field("printer", printer)
    add_field("job_seq", str(job_seq))
    parts.append(f"--{boundary}\r\n".encode())
    parts.append(
        b'Content-Disposition: form-data; name="stl"; filename="cube.stl"\r\n'
        b"Content-Type: application/octet-stream\r\n\r\n"
    )
    parts.append(stl_bytes)
    parts.append(b"\r\n")
    parts.append(f"--{boundary}--\r\n".encode())
    body = b"".join(parts)

    req = urllib.request.Request(
        f"{server}/api/tag",
        data=body,
        headers={"Content-Type": f"multipart/form-data; boundary={boundary}"},
        method="POST",
    )
    with urllib.request.urlopen(req, timeout=30) as r:
        assert r.status == 200
        resp = json.loads(r.read())

    # Manifest sanity
    assert resp["manifest"]["order_id"] == order_id
    assert resp["manifest"]["printer_id"]
    assert resp["manifest"]["master_stl_sha256"]
    assert resp["manifest"]["tagged_stl_sha256"]
    assert resp["size_stl"] > 0
    assert resp["size_gcode"] > 0
    assert resp["tagged_stl_b64"]
    assert resp["tagged_gcode_b64"]

    # Decode gcode → L1 round trip
    gcode = base64.b64decode(resp["tagged_gcode_b64"]).decode()
    sys.path.insert(0, str(REPO / "src"))
    from ghostprint_core import l1_extract_from_gcode, l2_verify_match

    l1 = l1_extract_from_gcode(gcode)
    assert l1["printer_id"] == resp["manifest"]["printer_id"], (
        f"L1 printer_id mismatch: {l1['printer_id']} vs {resp['manifest']['printer_id']}"
    )
    assert l1["job_seq"] == job_seq, f"L1 job_seq mismatch: {l1['job_seq']} vs {job_seq}"

    # L2 round trip — diff tagged STL against the master we sent
    tagged_path = Path(tempfile.mkdtemp()) / "tagged.stl"
    tagged_path.write_bytes(base64.b64decode(resp["tagged_stl_b64"]))
    master_path = Path(tempfile.mkdtemp()) / "master.stl"
    master_path.write_bytes(stl_bytes)

    import stl

    tagged = stl.mesh.Mesh.from_file(str(tagged_path))
    master = stl.mesh.Mesh.from_file(str(master_path))
    assert l2_verify_match(tagged, master, order_id) is True, "L2 verify should pass for correct order_id"
    assert l2_verify_match(tagged, master, "ORDER-WRONG-XYZ") is False, "L2 verify should fail for wrong order_id"


def test_tag_rejects_non_stl(server):
    import urllib.request
    boundary = "----gp-test-boundary-x7y9z"
    body = (
        f"--{boundary}\r\n"
        f'Content-Disposition: form-data; name="order_id"\r\n\r\nX\r\n'
        f"--{boundary}\r\n"
        f'Content-Disposition: form-data; name="printer"\r\n\r\np\r\n'
        f"--{boundary}\r\n"
        f'Content-Disposition: form-data; name="job_seq"\r\n\r\n1\r\n'
        f"--{boundary}\r\n"
        f'Content-Disposition: form-data; name="stl"; filename="x.stl"\r\n'
        f"Content-Type: text/plain\r\n\r\n"
        f"this is plain text not an stl"
        f"\r\n--{boundary}--\r\n"
    ).encode()
    req = urllib.request.Request(
        f"{server}/api/tag",
        data=body,
        headers={"Content-Type": f"multipart/form-data; boundary={boundary}"},
        method="POST",
    )
    try:
        urllib.request.urlopen(req, timeout=5)
        pytest.fail("expected 400 for non-STL upload")
    except urllib.error.HTTPError as e:
        assert e.code == 400
        body = json.loads(e.read())
        assert "STL" in body["error"]
