#!/usr/bin/env python3
"""
test_landing_serve.py — end-to-end test for the landing/serve.py wrapper.

Starts the HTTP server on an ephemeral port, hits GET / and POST /api/tag
with a real STL generated by the test itself, then:
  - asserts the response includes a valid manifest
  - asserts the L1 watermark in the returned gcode decodes back to the
    printer_id + job_seq + ts we sent
  - asserts the L2 watermark in the returned STL diffs back to the
    order_id we sent
  - asserts a wrong order_id fails the verify

Pure stdlib (no requests, no pytest-flask). ~5 s on a warm venv.
"""
from __future__ import annotations

import base64
import io
import json
import socket
import struct
import subprocess
import sys
import tempfile
import threading
import time
from pathlib import Path

import pytest

REPO = Path(__file__).resolve().parent.parent
SERVE = REPO / "landing" / "serve.py"


def _free_port() -> int:
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.bind(("127.0.0.1", 0))
        return s.getsockname()[1]


def _make_binary_stl(size_mm: float = 30.0) -> bytes:
    """Generate a tiny binary STL for upload. ~24 KB."""
    import numpy as np
    import stl

    mesh = stl.mesh.Mesh(np.zeros(12, dtype=stl.mesh.Mesh.dtype))
    h = size_mm / 2.0
    # 12 triangles of a cube
    data = np.array(
        [
            # +Z
            [[-h, -h, h], [h, -h, h], [h, h, h]],
            [[-h, -h, h], [h, h, h], [-h, h, h]],
            # -Z
            [[-h, -h, -h], [h, h, -h], [h, -h, -h]],
            [[-h, -h, -h], [-h, h, -h], [h, h, -h]],
            # +X
            [[h, -h, -h], [h, h, -h], [h, h, h]],
            [[h, -h, -h], [h, h, h], [h, -h, h]],
            # -X
            [[-h, -h, -h], [-h, h, h], [-h, h, -h]],
            [[-h, -h, -h], [-h, -h, h], [-h, h, h]],
            # +Y
            [[-h, h, -h], [h, h, -h], [h, h, h]],
            [[-h, h, -h], [h, h, h], [-h, h, h]],
            # -Y
            [[-h, -h, -h], [h, -h, h], [h, -h, -h]],
            [[-h, -h, -h], [-h, -h, h], [h, -h, h]],
        ],
        dtype=np.float32,
    )
    mesh.vectors = data
    buf = io.BytesIO()
    mesh.save("cube", buf)
    return buf.getvalue()


@pytest.fixture(scope="module")
def server():
    port = _free_port()
    env = dict(__import__("os").environ, PATH=__import__("os").environ["PATH"])
    proc = subprocess.Popen(
        [sys.executable, str(SERVE), "--host", "127.0.0.1", "--port", str(port)],
        cwd=str(REPO),
        env=env,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    # Wait for /health to come up
    import urllib.request
    deadline = time.time() + 15
    base = f"http://127.0.0.1:{port}"
    while time.time() < deadline:
        try:
            with urllib.request.urlopen(f"{base}/health", timeout=1) as r:
                if r.status == 200:
                    break
        except Exception:
            time.sleep(0.2)
    else:
        proc.terminate()
        out, err = proc.communicate(timeout=5)
        pytest.fail(f"serve.py did not come up in 15 s. stderr:\n{err.decode()}")
    yield base
    proc.terminate()
    try:
        proc.wait(timeout=5)
    except subprocess.TimeoutExpired:
        proc.kill()


